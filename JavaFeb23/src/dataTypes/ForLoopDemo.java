package dataTypes;

public class ForLoopDemo {

	public static void main(String[] args) {
		
		/**
		 * for(initialization, how far/end point, how will it increase/decrease)
		 * {
		 * Code to be executed
		 * }
		 */
		
		//write "I love you" 10 times using for loop
		
		for(int i = 1; i<=10; i++) {
			System.out.println("I love you");
		}

	}

}
